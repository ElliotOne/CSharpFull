namespace _01_Basic
{
    public class _04_ArithmeticOperators
    {
        void SimpleMethod()
        {
            int x = 6;
            int y = 2;
            int sum = x + y;
            int sub = x - y;
            int div = x / 2;
            int rem = x % y;
            int mul = x * y;


            //Increment and decrement operators
            int other = 5;
            other++;
            --other;


            //Short format of logical operators
            //+= , -= , *= , /= , ^= , %=
            other *= 10;
        }
    }
}
